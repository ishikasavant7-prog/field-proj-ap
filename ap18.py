# print() function in python
rad = 13
areac=3.14+(rad*rad)
# concatanation method with + symbol
print('radius='+ str(rad)+'area of circle ='+ str(areac))
# comma as data separator method 
print('radius=',rad,'area of circle=',areac)
# print () with formatted () function
print('radius ={} area of circle ={}'.format(rad,areac))
# print formatted string method
print('radius={rad} area of circle={areac}')
